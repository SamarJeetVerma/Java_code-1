package com.samar1mvc;

public class AddService 
{
    public float add(float i,float j)
    {
    	return i+j;
    }
    
    public float subtract(float i1,float j1)
    {
    	return i1-j1;
    }
    
    public float multiply(float i2,float j2)
    {
    	return i2*j2;
    }
    
    public float divide(float i3,float j3)
    {
    	return i3/j3;
    }
}

